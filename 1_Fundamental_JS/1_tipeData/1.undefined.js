/* 
! Undefined

* Tipe data ini terbentuk ketika sebuah variabel tidak memiliki nilai. Artinya, ketika kita mendeklarasikan variabel
* tanpa menginisialisasikan nilainya, variabel tersebut menjadi undefined. Contoh:
*/

let x;
console.log(typeof(x));

/* output: undefined */

/*
* Pada contoh kode di atas, kita mendeklarasikan variabel x, tetapi kita tidak menginisialisasikan dengan nilai apa pun.
* Ketika kita memastikan tipe data dengan menggunakan fungsi typeof(), ia akan menghasilkan output undefined.
 */